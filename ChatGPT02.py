from datetime import datetime, timedelta

def последний_день_февраля(год):
    первое_марта = datetime(год, 3, 1)
    последний_день_февраля = первое_марта - timedelta(days=1)
    return последний_день_февраля

год = 2023  # Замените на нужный вам год
последний_день = последний_день_февраля(год)
print(f"Последний день февраля {год} года: {последний_день.strftime('%Y-%m-%d')}")
