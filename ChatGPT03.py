# Вы можете использовать модуль calendar в Python для определения последнего дня
# февраля в заданном году.
# Функция calendar.monthrange(год, 2) возвращает кортеж, содержащий первый день
# недели и количество дней в феврале для заданного года.
# Мы используем второй элемент этого кортежа, чтобы получить количество дней, и
# затем форматируем строку с датой.

import calendar

def последний_день_февраля(год):
    последний_день = calendar.monthrange(год, 2)[1]
    дата = f"{год}-02-{последний_день}"
    return дата

год = 2023  # Замените на нужный вам год
последний_день = последний_день_февраля(год)
print(f"Последний день февраля {год} года: {последний_день}")
